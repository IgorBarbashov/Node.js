const os = require('os');

// Платформа
console.log(os.platform());

// Архитектура
console.log(os.arch());

// Информация
console.log(os.cpus());

// Свобоная память
console.log(os.freemem());

// Всего памяти
console.log(os.totalmem());

// Корневая директория на компьютере
console.log(os.homedir());

// Сколько система работает
console.log(os.uptime());